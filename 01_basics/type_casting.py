# string to int
age = int("19")
print(age, type(age))

# string to float
gpa = float("3.8")
print(gpa, type(gpa))

# int to string
name = str(100)
print(name, type(name))

# int to float
x = float(5)
print(x, type(x))

# float to int
y = int(3.9)
print(y, type(y))